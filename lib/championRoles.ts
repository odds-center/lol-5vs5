function toSet(arr: string[]): Set<string> {
  return new Set(arr);
}

const ROLE_SETS: Record<Role, Set<string>> = {
  top: toSet(TOP),
  jungle: toSet(JUNGLE),
  mid: toSet(MID),
  adc: toSet(ADC),
  support: toSet(SUPPORT),
};

/** 해당 포지션에 속하는 챔피언 이름 집합. */
export function getChampionNamesByRole(role: Role): Set<string> {
  return ROLE_SETS[role];
}

/** 챔피언 이름이 해당 포지션에 속하는지. */
export function isChampionInRole(championName: string, role: Role): boolean {
  return ROLE_SETS[role].has(championName);
}

/**
 * 역할 필터가 있을 때 검색 결과를 포지션으로 한 번 더 필터.
 * roleFilter가 null이면 필터 없음(전체).
 */
export function filterByRole(
  championNames: string[],
  roleFilter: Role | null,
): string[] {
  if (!roleFilter) return championNames;
  const set = ROLE_SETS[roleFilter];
  return championNames.filter((name) => set.has(name));
}
